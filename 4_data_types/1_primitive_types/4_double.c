// Program to demonstrate use of double data type

#include <stdio.h>

// main function
int main()
{
    double a = 123123123.00;
    double b = 123.123123;
    double c = 123123123123.123132;

    printf("Value of a: %lf\n", a);
    printf("Value of b: %lf\n", b);
    printf("Value of c: %lf\n", c);

    return 0;
}